    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try{
            jadwal j = new jadwal();
            this.stat = k.getCon().prepareStatement("insert into data_jadwal_mata_pelajaran values(?,?,?,?,?)");
            this.stat.setInt(1, 0);
            this.stat.setInt(2, j.id_kelas);
            this.stat.setInt(3, j.id_mapel);
            this.stat.setString(4, j.ruangan);
            this.stat.setString(5, j.hari);
            this.stat.executeUpdate();
            refreshTable();
//            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        try{
            jadwal j = new jadwal();
            j.id_jadwal = Integer.parseInt(txtId.getText());
            this.stat = k.getCon().prepareStatement("update data_jadwal_mata_pelajaran set hari=?," 
                    + "id_kelas=?,id_mapel=?,ruangan=?" 
                    + "where id_jadwal=?");
            this.stat.setString(1, j.hari);
            this.stat.setInt(2, j.id_kelas);
            this.stat.setInt(3, j.id_mapel);
            this.stat.setString(4, j.ruangan);
            this.stat.setInt(5, j.id_jadwal);
            this.stat.executeUpdate();
            refreshTable();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

