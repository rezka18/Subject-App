    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try{
            jadwal j = new jadwal();
            j.id_jadwal = Integer.parseInt(txtId.getText());
            this.stat = k.getCon().prepareStatement("delete from data_jadwal_mata_pelajaran" 
                    + "where id_jadwal=?");
            this.stat.setInt(1, j.id_jadwal);
            this.stat.executeUpdate();
            refreshTable();
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Jadwal");
        model.addColumn("Kelas");
        model.addColumn("Mapel");
        model.addColumn("Ruangan");
        model.addColumn("Hari");

        try {
            String sql = "select j.id_jadwal, k.nama_kelas, j.id_mapel, j.ruangan, j.hari from data_jadwal_mata_pelajaran as j inner join data_kelas as k on j.id_jadwal = k.id_kelas WHERE " + 
                    cmbCari.getSelectedItem().toString()
                    + " LIKE '%" + txtCari.getText() + "%'";
            java.sql.Connection conn = (Connection)k.getCon();
            java.sql.Statement stn = conn.createStatement();
            java.sql.ResultSet res = stn.executeQuery(sql);
            
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),
                    res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7)});
            }
            tblJadwal.setModel(model);
            } catch (SQLException e) {
                System.out.println("Error " + e.getMessage());
        }
    }//GEN-LAST:event_btnCariActionPerformed

