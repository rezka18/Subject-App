    class jadwal extends frameJadwal{
        int id_jadwal,id_kelas,id_mapel;
        String ruangan,hari;
        
        public jadwal()throws SQLException{
            String combokelas = cmbKelas.getSelectedItem().toString();
            String combomapel = cmbMapel.getSelectedItem().toString();
            String[] arrkelas = combokelas.split(":");
            String[] arrmapel = combomapel.split(":");
            this.id_kelas = Integer.parseInt(arrkelas[0]);
            this.id_mapel = Integer.parseInt(arrmapel[0]);
            this.ruangan = arrkelas[2];
            this.hari = cmbHari.getSelectedItem().toString();
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Jadwal");
        model.addColumn("ID Kelas");
        model.addColumn("ID Mapel");
        model.addColumn("Ruangan");
        model.addColumn("Hari");
        tblJadwal.setModel(model);
        try{
            this.stat = k.getCon().prepareStatement("select * from data_jadwal_mata_pelajaran");
            this.rs = this.stat.executeQuery();
            while (rs.next()){
                Object[] data={
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5)
                };
                model.addRow(data);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void refreshComboKelas(){
        try{
            this.stat = k.getCon().prepareStatement("select * from data_kelas");
            this.rs = this.stat.executeQuery();
            while (rs.next()){
                cmbKelas.addItem(rs.getString("id_kelas")+":"+rs.getString("nama_kelas")+":"+rs.getString("ruangan"));
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void refreshComboMapel(){
        try{
            this.stat = k.getCon().prepareStatement("select * from data_mata_pelajaran");
            this.rs = this.stat.executeQuery();
            while (rs.next()){
                cmbMapel.addItem(rs.getString("id_mapel")+":"+rs.getString("nama_mapel"));
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

