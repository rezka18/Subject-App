    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try{
            guru g = new guru();
            this.stat = k.getCon().prepareStatement("insert into data_guru values(?,?,?,?,?,?,?)");
            this.stat.setInt(1, 0);
            this.stat.setInt(2, g.id_kelas);
            this.stat.setInt(3, g.id_mapel);
            this.stat.setString(4, g.nama_guru);
            this.stat.setString(5, g.mata_pelajaran);
            this.stat.setString(6, g.kelas);
            this.stat.setString(7, g.jam_mengajar);
            this.stat.executeUpdate();
            refreshTable();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        try{
            guru g = new guru();
            g.id_guru = Integer.parseInt(txtId.getText());
            this.stat = k.getCon().prepareStatement("update data_guru set nama_guru=?," 
                    + "id_kelas=?,id_mapel=?,mata_pelajaran=?,kelas=?,jam_mengajar=?" 
                    + "where id_guru=?");
            this.stat.setString(1, g.nama_guru);
            this.stat.setInt(2, g.id_kelas);
            this.stat.setInt(3, g.id_mapel);
            this.stat.setString(4, g.mata_pelajaran);
            this.stat.setString(5, g.kelas);
            this.stat.setString(6, g.jam_mengajar);
            this.stat.setInt(7, g.id_guru);
            this.stat.executeUpdate();
            refreshTable();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

