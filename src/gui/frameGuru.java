    class guru extends frameGuru{
        int id_guru,id_kelas,id_mapel;
        String nama_guru,mata_pelajaran,kelas,jam_mengajar;
        
        public guru()throws SQLException{
            this.nama_guru = txtNama.getText();
            String combokelas = cmbKelas.getSelectedItem().toString();
            String combomapel = cmbMapel.getSelectedItem().toString();
            String[] arrkelas = combokelas.split(":");
            String[] arrmapel = combomapel.split(":");
            this.id_kelas = Integer.parseInt(arrkelas[0]);
            this.id_mapel = Integer.parseInt(arrmapel[0]);
            kelas = arrkelas[1];
            mata_pelajaran = arrmapel[1];
            this.jam_mengajar = txtJam.getText();
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Guru");
        model.addColumn("ID Kelas");
        model.addColumn("ID Mapel");
        model.addColumn("Nama Guru");
        model.addColumn("Mata Pelajaran");
        model.addColumn("Kelas");
        model.addColumn("Jam Mengajar");
        tblGuru.setModel(model);
        try{
            this.stat = k.getCon().prepareStatement("select * from data_guru");
            this.rs = this.stat.executeQuery();
            while (rs.next()){
                Object[] data={
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7)
                };
                model.addRow(data);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        txtId.setText("");
        txtNama.setText("");
        txtJam.setText("");
    }
    
    public void refreshComboKelas(){
        try{
            this.stat = k.getCon().prepareStatement("select * from data_kelas");
            this.rs = this.stat.executeQuery();
            while (rs.next()){
                cmbKelas.addItem(rs.getString("id_kelas")+":"+rs.getString("nama_kelas"));
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
    public void refreshComboMapel(){
        try{
            this.stat = k.getCon().prepareStatement("select * from data_mata_pelajaran");
            this.rs = this.stat.executeQuery();
            while (rs.next()){
                cmbMapel.addItem(rs.getString("id_mapel")+":"+rs.getString("nama_mapel"));
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
